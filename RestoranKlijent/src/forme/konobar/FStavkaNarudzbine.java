    private void comboKategorijaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboKategorijaItemStateChanged
        List<Potkategorija> lista = new LinkedList<>();
        Kategorija k = (Kategorija) comboKategorija.getSelectedItem();
        for (Potkategorija pk : SingletonHolder.getInstance().getPotkategorije()) {
            if (pk.getKategorija().equals(k)) {
                lista.add(pk);
            }
        }
        comboPotkategorija.setModel(new DefaultComboBoxModel(lista.toArray()));
    }//GEN-LAST:event_comboKategorijaItemStateChanged

